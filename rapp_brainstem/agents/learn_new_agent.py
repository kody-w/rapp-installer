    def _install_dependencies(self, packages):
        if not packages:
            return {"success": True}
        try:
            import sys
            for pkg in packages:
                result = subprocess.run(
                    [sys.executable, '-m', 'pip', 'install', '--quiet', pkg],
                    capture_output=True, text=True, timeout=60
                )
                if result.returncode != 0:
                    return {"success": False,
                            "error": f"pip install {pkg} failed: {result.stderr}"}
            return {"success": True, "installed": packages}
        except subprocess.TimeoutExpired:
            return {"success": False, "error": "pip install timed out"}
        except Exception as e:
            return {"success": False, "error": str(e)}

    # ── List / Delete ─────────────────────────────────────────────────────

    def _list_generated_agents(self):
        agents = []
        for f in sorted(self.agents_dir.glob('*_agent.py')):
            if f.name in CORE_AGENTS:
                continue
            content = f.read_text()
            is_generated = 'Auto-generated by LearnNewAgent' in content
            agents.append({
                "name": f.stem.replace('_agent', ''),
                "file": f.name,
                "auto_generated": is_generated
            })
        return json.dumps({
            "status": "success",
            "agents": agents,
            "count": len(agents)
        })

    def _delete_agent(self, name):
        if not name:
            return json.dumps({
                "status": "error",
                "message": "Please provide the agent name to delete."
            })

        target = None
        # 1. Exact agent-name match (the loader's real key, found via AST scan).
        for nm, fn in self._existing_agent_names().items():
            if nm.lower() == name.lower():
                target = fn
                break
        # 2. Exact filename slug.
        if not target:
            slug_file = f"{self._slug(self._sanitize_name(name))}_agent.py"
            if (self.agents_dir / slug_file).exists():
                target = slug_file
        # 3. Deterministic substring among NON-core files; report ambiguity rather
        #    than guessing (a filesystem-order-dependent unlink is a footgun).
        if not target:
            matches = [f.name for f in sorted(self.agents_dir.glob('*_agent.py'))
                       if name.lower() in f.name.lower() and f.name not in CORE_AGENTS]
            if len(matches) == 1:
                target = matches[0]
            elif len(matches) > 1:
                return json.dumps({
                    "status": "ambiguous",
                    "message": f"'{name}' matches multiple agents — be more specific.",
                    "candidates": matches
                })

        if not target:
            return json.dumps({"status": "error", "message": f"Agent '{name}' not found."})

        if target in CORE_AGENTS:
            return json.dumps({"status": "error",
                "message": f"Cannot delete core agent '{target}'."})

        try:
            (self.agents_dir / target).unlink()
            return json.dumps({
                "status": "success",
                "message": f"Deleted agent '{name}'",
                "file": str(self.agents_dir / target)
            })
        except Exception as e:
            return json.dumps({"status": "error", "message": str(e)})


