    # ── Single agent creation ─────────────────────────────────────────────

    def _create_agent(self, description, name='', agent_parameters='',
                      agent_logic='', write=True, **kwargs):
        if not description and not name:
            return json.dumps({
                "status": "error",
                "message": "Please provide agent_description (and ideally agent_logic — "
                           "the Python code for what the agent should do)."
            })

        if not name:
            name = self._generate_name(description)

        name = self._sanitize_name(name)
        if not re.match(r'^[A-Z][a-zA-Z0-9]*$', name):
            return json.dumps({
                "status": "error",
                "message": f"Invalid agent name '{name}'. Use CamelCase (e.g. 'WebSummarizer')."
            })

        class_name = f"{name}Agent"
        # Filename slug matches SwarmFactory's convention (strip to [a-z0-9]) so the
        # two tools map the same name to the same file and never silently shadow.
        slug = self._slug(name)
        file_name = f"{slug}_agent.py"
        file_path = self.agents_dir / file_name

        if write and file_path.exists():
            return json.dumps({
                "status": "error",
                "message": f"Agent '{name}' already exists at {file_path}. "
                           f"Delete it first or choose a different name."
            })

        # Also refuse if a DIFFERENT file already registers this agent name — the
        # loader keys on self.name, so two files with one name silently collide.
        if write:
            clash = self._existing_agent_names().get(name)
            if clash and clash != file_name:
                return json.dumps({
                    "status": "error",
                    "message": f"An agent named '{name}' already exists in agents/{clash}. "
                               f"Choose a different name."
                })

        agent_code = self._generate_agent_code(
            description or name, name, class_name,
            agent_parameters, agent_logic, **kwargs)

        # Syntax-check before writing so a bad agent_logic never lands a broken file.
        syntax_err = self._syntax_check(agent_code)
        if syntax_err:
            return json.dumps({
                "status": "error",
                "message": f"Generated agent has a syntax error: {syntax_err}. "
                           f"Check the agent_logic you supplied (it becomes the body of perform()).",
                "code": agent_code,
            })

        if not write:
            return json.dumps({
                "status": "ok",
                "action": "preview",
                "filename": file_name,
                "class_name": class_name,
                "display_name": name,
                "lines": len(agent_code.split('\n')),
                "code": agent_code,
                "message": f"Preview of {file_name} — use action='create' to write it."
            })

        try:
            file_path.write_text(agent_code)
        except Exception as e:
            return json.dumps({"status": "error", "message": f"Failed to write agent file: {e}"})

        hot_load_result = self._hot_load_agent(file_path, class_name)

        result = {
            "status": "success",
            "action": "create",
            "message": f"Created and loaded agent '{name}'",
            "agent_name": name,
            "filename": file_name,
            "file_path": str(file_path),
            "lines": len(agent_code.split('\n')),
            "hot_loaded": hot_load_result.get("success", False),
            "description": (description or name)[:200],
            "hint": (
                f"Agent saved to agents/{file_name} — it auto-loads on the next request. "
                f"Edit the perform() method to customize the logic. "
                f"To submit to RAR, re-run with action='submit'."
            ),
        }

        if hot_load_result.get("installed_deps"):
            result["installed_dependencies"] = hot_load_result["installed_deps"]
        if not hot_load_result.get("success"):
            result["hot_load_error"] = hot_load_result.get("error")
            if hot_load_result.get("hint"):
                result["hot_load_hint"] = hot_load_result["hint"]

        return json.dumps(result)

    # ── Swarm creation ────────────────────────────────────────────────────

    def _create_swarm(self, description, swarm_name='', write=True, **kwargs):
        specs = self._parse_swarm_specs(kwargs.get('swarm_agents'),
                                        kwargs.get('agents_in_swarm'))
        if not specs:
            return json.dumps({
                "status": "error",
                "message": "Please provide swarm_agents (a JSON array of {name, description, "
                           "logic} stages) or agents_in_swarm (comma-separated roles)."
            })

        if not swarm_name:
            swarm_name = self._generate_name(description or specs[0]['name'])
        swarm_name = self._sanitize_name(swarm_name)
        if not re.match(r'^[A-Z][a-zA-Z0-9]*$', swarm_name):
            return json.dumps({"status": "error",
                "message": f"Invalid swarm name '{swarm_name}'. Use CamelCase (e.g. 'ResearchPipeline')."})
        swarm_key = self._slug(swarm_name)
        filename = f"{swarm_key}_agent.py"
        class_name = swarm_name

        if not description:
            description = (f"{swarm_name} — a converged swarm pipeline of "
                           + " -> ".join(s['name'] for s in specs) + ".")

        category = kwargs.get('category', 'pipeline')
        namespace = (kwargs.get('namespace', '') or 'rapp').lstrip('@')
        env_list = [e.strip() for e in (kwargs.get('requires_env', '') or '').split(",") if e.strip()]
        tags = self._generate_tags(description) + ["swarm", "singleton", "composite"]

        # ── Resolve each stage into an inlined _Internal* persona ──
        stages = []
        seen = set()
        for spec in specs:
            role_name = self._sanitize_name(spec['name'])
            if not re.match(r'^[A-Z][a-zA-Z0-9]*$', role_name):
                return json.dumps({"status": "error",
                    "message": f"Invalid stage name '{spec['name']}'. Use CamelCase."})
            role_key = self._slug(role_name)
            if role_key in seen:
                return json.dumps({"status": "error",
                    "message": f"Duplicate stage '{role_name}' in the swarm."})
            if role_name == swarm_name:
                return json.dumps({"status": "error",
                    "message": f"Stage '{role_name}' collides with the swarm name. Rename the stage."})
            seen.add(role_key)
            stages.append({
                "name": role_name,
                "key": role_key,
                "internal": f"_Internal{role_name}",
                "desc": (spec.get('description') or f"{role_name} stage").strip(),
                "logic": (spec.get('logic') or '').strip() or self._default_swarm_logic(role_key),
            })

        code = self._assemble_swarm_singleton(
            swarm_name, class_name, swarm_key, description, stages,
            category, namespace, env_list, tags)

        syntax_err = self._syntax_check(code)
        if syntax_err:
            return json.dumps({
                "status": "error",
                "message": f"Generated swarm has a syntax error: {syntax_err}. "
                           f"Check the per-stage logic you supplied.",
                "code": code,
            })

        if not write:
            return json.dumps({
                "status": "ok",
                "action": "preview",
                "swarm_name": swarm_name,
                "filename": filename,
                "class_name": class_name,
                "stages": [s["name"] for s in stages],
                "lines": len(code.split("\n")),
                "code": code,
                "message": f"Preview of converged swarm singleton {filename} — use action='swarm' to write it.",
            })

        # Refuse to clobber a file, or shadow an agent already claiming this name
        # under a different filename (the loader's real join key is self.name).
        file_path = self.agents_dir / filename
        if file_path.exists():
            return json.dumps({"status": "error",
                "message": f"agents/{filename} already exists. Delete it or choose a different swarm name."})
        clash = self._existing_agent_names().get(swarm_name)
        if clash and clash != filename:
            return json.dumps({"status": "error",
                "message": f"An agent named '{swarm_name}' already exists in agents/{clash}. "
                           f"Choose a different swarm name."})

        try:
            file_path.write_text(code)
        except Exception as e:
            return json.dumps({"status": "error", "message": f"Failed to write {filename}: {e}"})

        hot = self._hot_load_agent(file_path, class_name)

        result = {
            "status": "success",
            "action": "swarm",
            "swarm_name": swarm_name,
            "filename": filename,
            "file_path": str(file_path),
            "class_name": class_name,
            "stages": [s["name"] for s in stages],
            "lines": len(code.split("\n")),
            "hot_loaded": hot.get("success", False),
            "message": (
                f"Created converged swarm singleton '{swarm_name}' "
                f"({len(stages)} inlined stages, one file). It hot-loads as a SINGLE "
                f"discoverable agent and runs the full pipeline on call; the stage "
                f"personas are hidden (_Internal*). Share agents/{filename} as-is — it is "
                f"already a self-contained SwarmFactory-style singleton."
            ),
        }
        if not hot.get("success"):
            result["hot_load_error"] = hot.get("error")
        return json.dumps(result)

    def _assemble_swarm_singleton(self, swarm_name, class_name, slug, description,
                                  stages, category, namespace, env_list, tags):
        """Assemble ONE converged singleton file: inlined _Internal stage personas
        behind a single public BasicAgent entrypoint that pipelines them. This is
        the canonical swarm form — same shape as a SwarmFactory singleton."""
        date = datetime.now().strftime("%Y-%m-%d %H:%M")
        raw_desc = description.replace('\n', ' ')[:300]          # for json.dumps (it escapes)
        safe_desc = raw_desc.replace('"', '\\"')                 # for f-string-into-source
        all_logic = "\n".join(s["logic"] for s in stages)
        imports = "\n".join(self._infer_imports(all_logic, description))
        stage_doc = "\n".join(
            f"  {i + 1}. {s['name']}" + (f" - {s['desc']}" if s['desc'] else "")
            for i, s in enumerate(stages))

        manifest = {
            "schema": "rapp-agent/1.0",
            "name": f"@{namespace}/{slug}",
            "version": "1.0.0",
            "display_name": swarm_name,
            "description": raw_desc,
            "author": namespace,
            "tags": tags,
            "category": category,
            "quality_tier": "community",
            "requires_env": env_list,
            "dependencies": ["@rapp/basic_agent"],
            "delegates_to_inlined": [s["name"] for s in stages],
            "example_call": {"args": {"task": f"Run the {swarm_name} pipeline"}},
        }

        L = []
        L.append('"""')
        L.append(description.replace('"""', "'''"))
        L.append("")
        L.append(f"{swarm_name} - a converged swarm singleton. ONE file: each stage is an")
        L.append("inlined _Internal persona (hidden from *Agent discovery) and a single public")
        L.append("entrypoint orchestrates them. Drop into any RAPP brainstem's agents/ directory")
        L.append("and it works - no sibling files, no imports between stages.")
        L.append("")
        L.append('Stages (run in order; each may return a "data_slush" dict that the next')
        L.append("stage reads via pipeline_data):")
        L.append(stage_doc)
        L.append("")
        L.append(f"Auto-generated by LearnNewAgent on {date}.")
        L.append("Compatible with the RAR registry at https://github.com/kody-w/RAR")
        L.append('"""')
        L.append("")
        L.append(imports)
        L.append("")
        L.append("try:")
        L.append("    from agents.basic_agent import BasicAgent")
        L.append("except ImportError:")
        L.append("    from basic_agent import BasicAgent")
        L.append("")
        L.append("")
        L.append("__manifest__ = " + json.dumps(manifest, indent=4))
        L.append("")
        L.append("")
        L.append("# --- Inlined stage personas (prefixed _Internal to hide from discovery) ---")
        for s in stages:
            doc = (s['desc'] or f"{s['name']} stage").replace('"', "'").replace("\n", " ")
            L.append("")
            L.append(f"class {s['internal']}:")
            L.append(f'    """{doc}"""')
            L.append("    def run(self, task='', **kwargs):")
            L.append("        pipeline_data = kwargs.get('pipeline_data', {})")
            L.append("        try:")
            L.append(self._indent_code(s['logic'], 12))
            L.append("        except Exception as _e:")
            L.append(f'            return json.dumps({{"status": "error", "stage": "{s["key"]}", "message": str(_e)}})')
        L.append("")
        L.append("")
        L.append("# --- PUBLIC ENTRYPOINT (the only discoverable agent) ---")
        L.append("")
        L.append(f"class {class_name}(BasicAgent):")
        L.append("    def __init__(self):")
        L.append(f'        self.name = "{swarm_name}"')
        L.append("        self.metadata = {")
        L.append('            "name": self.name,')
        L.append(f'            "description": "{safe_desc}",')
        L.append('            "parameters": {')
        L.append('                "type": "object",')
        L.append('                "properties": {')
        L.append(f'                    "task": {{"type": "string", "description": "What you want the {swarm_name} swarm to do"}}')
        L.append("                },")
        L.append('                "required": ["task"]')
        L.append("            }")
        L.append("        }")
        L.append("        super().__init__(name=self.name, metadata=self.metadata)")
        L.append("")
        L.append("    def perform(self, **kwargs):")
        L.append("        task = kwargs.get('task', '')")
        L.append("        results = {}")
        L.append("        slush = {}")
        L.append("        stages = [")
        for s in stages:
            L.append(f'            ("{s["key"]}", {s["internal"]}()),')
        L.append("        ]")
        L.append("        for _name, _stage in stages:")
        L.append("            r = _stage.run(task=task, pipeline_data=dict(slush))")
        L.append("            results[_name] = r")
        L.append("            try:")
        L.append("                parsed = json.loads(r)")
        L.append("                if isinstance(parsed, dict) and 'data_slush' in parsed:")
        L.append("                    slush.update(parsed['data_slush'])")
        L.append("            except (json.JSONDecodeError, TypeError):")
        L.append("                pass")
        L.append("        return json.dumps({")
        L.append('            "status": "ok",')
        L.append(f'            "swarm": "{swarm_name}",')
        L.append('            "pipeline_steps": len(stages),')
        L.append('            "results": results,')
        L.append("        })")
        L.append("")
        L.append("")
        L.append(f"class {class_name}Agent({class_name}):")
        L.append("    pass")
        L.append("")
        L.append("")
        L.append('if __name__ == "__main__":')
        L.append(f'    print({class_name}().perform(task="test"))')
        L.append("")
        return "\n".join(L)

    def _parse_swarm_specs(self, swarm_agents, agents_in_swarm):
        """Build a list of {name, description, parameters, logic} sub-agent specs.

        Prefers the structured `swarm_agents` JSON (real logic per stage); falls back
        to `agents_in_swarm` role names (stub stages); finally a default trio.
        """
        specs = []
        data = swarm_agents
        if isinstance(data, str) and data.strip():
            try:
                data = json.loads(data)
            except Exception:
                data = None
        if isinstance(data, dict):
            data = data.get('agents') or data.get('sub_agents') or data.get('stages')
        if isinstance(data, list):
            for el in data:
                if isinstance(el, str) and el.strip():
                    specs.append({"name": el})
                elif isinstance(el, dict):
                    name = el.get('name') or el.get('role')
                    if not name:
                        continue
                    specs.append({
                        "name": name,
                        "description": el.get('description', ''),
                        "parameters": el.get('parameters', ''),
                        "logic": el.get('logic', ''),
                    })

        if not specs and agents_in_swarm:
            specs = [{"name": r.strip()} for r in agents_in_swarm.split(",") if r.strip()]

        if not specs:
            specs = [{"name": r} for r in ("Researcher", "Processor", "Formatter")]
        return specs

