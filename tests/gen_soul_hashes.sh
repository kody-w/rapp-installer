#!/usr/bin/env bash
#
# gen_soul_hashes.sh — regenerate rapp_brainstem/soul_defaults.sha256
#
# Walks the full git history of rapp_brainstem/soul.md and records the NORMALIZED
# SHA-256 (see rapp_brainstem/soul_hash.py) of every default soul the project has
# ever shipped, plus the current working-tree default. install.sh / install.ps1
# consult this manifest on upgrade: if an installed soul.md normalizes to one of
# these hashes it is an unmodified historical default and is safe to refresh; any
# other soul is a user customization and is preserved untouched.
#
# Run this whenever rapp_brainstem/soul.md changes — a pytest in
# test_local_agents.py fails CI if the manifest is stale:
#
#   bash tests/gen_soul_hashes.sh
#
# Deterministic: output is sorted, so re-running with no soul change is a no-op diff.
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
SOUL_REL="rapp_brainstem/soul.md"
HASHER="$REPO_ROOT/rapp_brainstem/soul_hash.py"
OUT="$REPO_ROOT/rapp_brainstem/soul_defaults.sha256"
PY="${PYTHON:-python3}"

cd "$REPO_ROOT"

hash_stream() { "$PY" "$HASHER" -; }   # reads a soul on stdin, prints its normalized sha256

rows="$(mktemp)"
trap 'rm -f "$rows"' EXIT

emit() { printf '%s\t%s\n' "$1" "$2" >> "$rows"; }   # <hash> <provenance>

# Every commit that ever touched the soul (--follow crosses any future renames).
while IFS= read -r c; do
    [ -n "$c" ] || continue
    # Resolve the soul's path as of this commit (rename-safe), then hash that blob.
    path="$SOUL_REL"
    if ! git cat-file -e "$c:$path" 2>/dev/null; then
        path="$(git ls-tree -r --name-only "$c" | grep -E '(^|/)soul\.md$' | grep -m1 rapp_brainstem || true)"
    fi
    [ -n "$path" ] || continue
    h="$(git show "$c:$path" 2>/dev/null | hash_stream || true)"
    [ -n "$h" ] || continue
    emit "$h" "$(git log -1 --format='%h %s' "$c")"
done < <(git log --follow --format='%H' -- "$SOUL_REL")

# The current working-tree default (what THIS checkout ships / just shipped) — always
# recorded even if it is uncommitted, so the shipped soul is guaranteed present.
if [ -f "$REPO_ROOT/$SOUL_REL" ]; then
    emit "$(hash_stream < "$REPO_ROOT/$SOUL_REL")" "current working tree ($SOUL_REL)"
fi

# Write the manifest: documented header, then one line per UNIQUE hash with the
# newest commit that produced it as an inline provenance comment.
{
    echo "# soul_defaults.sha256 — normalized SHA-256 of every default soul.md ever shipped."
    echo "#"
    echo "# GENERATED by tests/gen_soul_hashes.sh — do not hand-edit. Regenerate after any"
    echo "# change to rapp_brainstem/soul.md (test_local_agents.py enforces this in CI)."
    echo "#"
    echo "# Normalization (rapp_brainstem/soul_hash.py, mirrored natively in install.ps1):"
    echo "#   strip UTF-8 BOM; CRLF->LF; strip trailing space/tab per line; exactly one"
    echo "#   trailing newline; then sha256 (lowercase hex)."
    echo "#"
    echo "# On upgrade the installers refresh soul.md ONLY if its normalized hash is listed"
    echo "# here (an unmodified historical default) AND differs from the new default. Any"
    echo "# other soul — or one that cannot be normalized — is preserved byte-for-byte."
    echo "#"
    echo "# Format: <64-hex normalized sha256>  # <representative commit>"
    awk -F'\t' '!seen[$1]++ { printf "%s  # %s\n", $1, $2 }' "$rows" | sort
} > "$OUT"

echo "Wrote $OUT ($(grep -cE '^[0-9a-f]{64}' "$OUT" || true) unique default hashes)"
